import getHeroesHealthStatus from '../basic';

const expectedList = [
  { name: 'маг', health: 100 },
  { name: 'лучник', health: 80 },
  { name: 'мечник', health: 10 },
];

test('get heroes health status', () => {
  const heroes = [
    { name: 'мечник', health: 10 },
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
  ];
  const result = getHeroesHealthStatus(heroes);
  expect(result).toEqual(expectedList);
});
